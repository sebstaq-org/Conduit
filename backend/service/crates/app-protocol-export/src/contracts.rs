fn push_schema<T: ZodSchema>(output: &mut String, name: &str) {
    output.push_str("// prettier-ignore\n");
    output.push_str("const ");
    output.push_str(name);
    output.push_str("Schema = ");
    output.push_str(&T::zod_schema());
    output.push_str(";\n// prettier-ignore\ntype ");
    output.push_str(name);
    output.push_str(" = z.infer<typeof ");
    output.push_str(name);
    output.push_str("Schema>;\n\n");
}

fn push_export_block(output: &mut String, keyword: &str, names: &[&str]) {
    output.push_str(keyword);
    output.push_str(" {\n");
    for name in names {
        output.push_str("  ");
        output.push_str(name);
        output.push_str(",\n");
    }
    output.push_str("};\n\n");
}

