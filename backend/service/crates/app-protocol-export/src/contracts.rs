fn object_schema(fields: &[(&str, &str)]) -> String {
    let fields = fields
        .iter()
        .map(|(name, schema)| format!("  {name}: {schema}"))
        .collect::<Vec<_>>()
        .join(",\n");
    format!("z.object({{\n{fields}\n}})")
}

fn literal_union(values: &[&str]) -> String {
    let values = values
        .iter()
        .map(|value| format!(r#"z.literal("{value}")"#))
        .collect::<Vec<_>>()
        .join(", ");
    format!("z.union([{values}])")
}

fn optional_meta() -> &'static str {
    "AcpMetaSchema.optional().nullable()"
}

fn optional_unknown() -> &'static str {
    "z.unknown().optional().nullable()"
}
